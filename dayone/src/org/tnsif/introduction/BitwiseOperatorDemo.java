//Program to demonstrate Bitwise operator
package org.tnsif.introduction;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
		int x=15,y=3;
		System.out.println(x&y);
		System.out.println(x|y);
		System.out.println(x^y);
		System.out.println(8>>1);
		System.out.println(8<<1);
		

	}

}
