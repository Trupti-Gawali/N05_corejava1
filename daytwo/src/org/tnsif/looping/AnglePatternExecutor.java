//program to demonstrate on angle pattern
package org.tnsif.looping;
public class AnglePatternExecutor {

	public static void main(String[] args) {
		int i, j, row=4;   
		 
		for(i=1; i<=row; i++)   
		{   
		
		for(j=1; j<=i; j++)   
		{   	   
		System.out.print(" * ");   
		}     
		System.out.println();   
		}   
	}

}
